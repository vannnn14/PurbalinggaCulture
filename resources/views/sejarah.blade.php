tah Hindia Belanda yang tersimpan dalam koleksi Aarsip Nasional Republik Indonesia.Berdasarkan sumber-sumber diatas, maka melalui Peraturan daerah (perda) No. 15 Tahun 1996 tanggal 19 Nopember 1996, ditetapkan bahwa hari jadi Kabupaten Purbalingga adalah 18 Desember 1830 atau 3 Rajab 1246 Hijriah atau 3 Rajab 1758 Je.      </p>   
